case_db() {
    cd_compose
    case "\${1:-connect}" in
        connect)
            docker compose exec postgres psql -U velo -d velo
            ;;
        dump)
            TIMESTAMP=\$(date +%Y%m%d_%H%M%S)
            DUMP_FILE="\$INSTALL_BASE/backups/velo_db_\${TIMESTAMP}.sql"
            mkdir -p "\$INSTALL_BASE/backups"
            docker compose exec -T postgres pg_dump -U velo velo > "\$DUMP_FILE"
            echo -e "\${GREEN}✓ Dump: \$DUMP_FILE\${NC}"
            ;;
        restore)
            if [ -z "\${2:-}" ]; then
                echo "Usage: velo db restore <file>"
                exit 1
            fi
            if [ ! -f "\$2" ]; then
                echo -e "\${RED}File not found: \$2\${NC}"
                exit 1
            fi
            echo -e "\${YELLOW}⚠ This will overwrite the current database!\${NC}"
            read -rp "Are you sure? (y/n): " -n 1 < /dev/tty
            echo
            if [[ \$REPLY =~ ^[Yy]\$ ]]; then
                echo "Restoring database..."
                cat "\$2" | docker compose exec -T postgres psql -U velo velo
                echo -e "\${GREEN}✓ Database restored\${NC}"
            fi
            ;;
        migrate)
            echo "Running Alembic migrations..."
            docker compose exec -T app python -m alembic upgrade head
            echo -e "\${GREEN}✓ Migrations complete\${NC}"
            ;;
        *)
            echo "Database commands:"
            echo "  velo db connect          -- Open psql session"
            echo "  velo db dump             -- Create SQL dump"
            echo "  velo db restore <file>   -- Restore from dump"
            echo "  velo db migrate          -- Run Alembic migrations"
            ;;
    esac
}

