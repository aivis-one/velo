# ==============================================================================
# UPDATE
# ==============================================================================
#
# Standard flow:
#   1. Show current commit + branch.
#   2. If uncommitted changes -- prompt to discard.
#   3. git fetch + git diff origin/BRANCH. If no diff -> "Already up to date",
#      exit. To bypass, pass --force.
#   4. git pull.
#   5. Rebuild backend image.
#   6. Down stack, up backend+postgres+redis (frontend deferred).
#   7. Wait for /health (process alive).
#   8. Run migrations.
#   9. Regenerate generated.ts from live OpenAPI.
#   10. If generated.ts changed -- bot-commit + push (single rebase retry).
#   11. Run backend pytest (informational -- doesn't abort the rest).
#   12. Rebuild frontend image (with fresh types).
#   13. Up frontend.
#   14. Final /ready check.
#
# --force:
#   Skips the "Already up to date" early exit. Useful when the working tree
#   was modified outside the normal pull flow (e.g. you manually checked out
#   a different branch) and you want to rebuild and re-deploy regardless.
# ==============================================================================

case_update() {
    cd_compose

    FORCE=0
    if [ "\${1:-}" = "--force" ]; then
        FORCE=1
    fi

    echo "=== Updating VELO ==="
    echo ""

    # Save current state.
    CURRENT_COMMIT=\$(git rev-parse --short HEAD)
    BRANCH=\$(git branch --show-current)
    echo "Current: \$CURRENT_COMMIT (\$BRANCH)"

    # Check uncommitted changes. We use git status --porcelain rather than
    # git diff-index --quiet because the latter false-positives on stat
    # metadata drift (chmod, touch, FS restore) even with identical content.
    if [ -n "\$(git status --porcelain)" ]; then
        echo -e "\${YELLOW}⚠ Uncommitted changes detected:\${NC}"
        git status --short
        echo ""
        read -rp "Discard local changes and update? (y/n): " -n 1 < /dev/tty
        echo
        if [[ ! \$REPLY =~ ^[Yy]\$ ]]; then
            echo "Update cancelled"
            return 0
        fi
        git checkout -- .
    fi

    # Fetch and compare. The deploy key alias picks up the right key.
    git fetch origin
    if git diff --quiet HEAD "origin/\$BRANCH" 2>/dev/null && [ "\$FORCE" = "0" ]; then
        echo -e "\${GREEN}✓ Already up to date\${NC} (use --force to rebuild anyway)"
        return 0
    fi

    if [ "\$FORCE" = "1" ] && git diff --quiet HEAD "origin/\$BRANCH" 2>/dev/null; then
        echo "Force update: rebuilding without new commits"
    else
        echo "Pulling updates..."
        if ! git pull origin "\$BRANCH"; then
            echo -e "\${RED}✗ git pull failed.\${NC}"
            echo "  Inspect: git -C \$COMPOSE_DIR status"
            return 1
        fi
        NEW_COMMIT=\$(git rev-parse --short HEAD)
        echo "Updated: \$CURRENT_COMMIT -> \$NEW_COMMIT"
    fi
    echo ""

    # Rebuild backend image.
    echo "Rebuilding backend image..."
    source_vite_env
    docker compose build app

    # Restart stack: down everything, up infra+backend first (frontend
    # waits for fresh generated.ts).
    echo "Restarting backend + infra..."
    docker compose down
    docker compose up -d app postgres redis

    # Wait for process to come up.
    echo "Waiting for backend..."
    for i in \$(seq 1 18); do
        if curl -sf "http://127.0.0.1:\${APP_PORT}/health" > /dev/null 2>&1; then
            echo -e "\${GREEN}✓ Backend up\${NC}"
            break
        fi
        if [ "\$i" = "18" ]; then
            echo -e "\${RED}✗ Backend did not respond within 90s\${NC}"
            docker compose logs --tail=30 app
            return 1
        fi
        echo -n "."
        sleep 5
    done
    echo ""

    # Migrations.
    echo "Running database migrations..."
    docker compose exec -T app python -m alembic upgrade head || {
        echo -e "\${RED}✗ Migration failed\${NC}"
        return 1
    }
    echo -e "\${GREEN}✓ Migrations applied\${NC}"

    # Regenerate types BEFORE pytest, so any tests that check generated
    # schema integrity see the current file.
    echo ""
    echo "Regenerating frontend types from live OpenAPI..."
    if ! curl -sf "http://127.0.0.1:\${APP_PORT}/openapi.json" -o /tmp/openapi.json; then
        echo -e "\${RED}✗ Cannot fetch /openapi.json from backend\${NC}"
        return 1
    fi
    python3 "\$COMPOSE_DIR/backend/scripts/generate_ts_types.py" \\
        /tmp/openapi.json \\
        "\$COMPOSE_DIR/frontend/src/api/generated.ts" || {
        echo -e "\${RED}✗ generate_ts_types.py failed\${NC}"
        rm -f /tmp/openapi.json
        return 1
    }
    rm -f /tmp/openapi.json

    # Bot-commit if generated.ts drifted.
    if [ -n "\$(git status --porcelain frontend/src/api/generated.ts)" ]; then
        echo "Schema drift detected -- committing regenerated generated.ts"
        git add frontend/src/api/generated.ts
        git -c user.name="\$BOT_NAME" -c user.email="\$BOT_EMAIL" commit -m \\
"chore(types): regenerate generated.ts

Triggered by velo update on commit \$(git rev-parse --short HEAD~)."

        PUSH_OK=0
        for attempt in 1 2; do
            if git push origin "\$BRANCH"; then
                PUSH_OK=1; break
            fi
            if [ "\$attempt" = "1" ]; then
                echo "Push failed (likely a parallel push). Rebasing and retrying..."
                git pull --rebase origin "\$BRANCH" || break
            fi
        done
        if [ "\$PUSH_OK" = "0" ]; then
            echo -e "\${RED}✗ Failed to push regenerated types\${NC}"
            echo "  Bot commit exists locally on \$COMPOSE_DIR but is not on origin."
            return 1
        fi
        echo -e "\${GREEN}✓ Bot pushed regenerated types\${NC}"
    else
        echo -e "\${GREEN}✓ Types are in sync\${NC}"
    fi

    # Backend tests -- informational only. We don't abort the rest of the
    # pipeline; the dev site stays usable so you can debug. We propagate
    # the failure as the script exit code at the end.
    echo ""
    echo "Running backend tests..."
    pytest_status=0
    docker compose exec -T app python -m pytest tests/ -v --tb=short \\
        || pytest_status=\$?
    if [ \$pytest_status -eq 0 ]; then
        echo -e "\${GREEN}✓ All backend tests passed\${NC}"
    else
        echo -e "\${RED}✗ Backend tests failed -- continuing with frontend build\${NC}"
    fi

    # Rebuild frontend with fresh types.
    echo ""
    echo "Building frontend (vue-tsc + vite + vitest run inside)..."
    docker compose build frontend || {
        echo -e "\${RED}✗ Frontend build failed -- backend is still running\${NC}"
        echo "  See output above for the actual error."
        return 1
    }
    docker compose up -d frontend

    # Final readiness check.
    echo ""
    echo "Waiting for /ready..."
    for i in \$(seq 1 12); do
        if curl -sf "http://127.0.0.1:\${APP_PORT}/ready" > /dev/null 2>&1; then
            echo -e "\${GREEN}✓ /ready -> 200\${NC}"
            break
        fi
        if [ "\$i" = "12" ]; then
            echo -e "\${YELLOW}⚠ /ready did not return 200 within 60s\${NC}"
        fi
        echo -n "."
        sleep 5
    done

    echo ""
    echo -e "\${GREEN}✓ Update complete\${NC}"

    # Propagate pytest exit code.
    if [ \$pytest_status -ne 0 ]; then
        return 1
    fi
}

