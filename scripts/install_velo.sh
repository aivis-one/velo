case_backup() {
    cd_compose
    TIMESTAMP=\$(date +%Y%m%d_%H%M%S)
    BACKUP_DIR="\$INSTALL_BASE/backups"
    BACKUP_FILE="\$BACKUP_DIR/velo_backup_\${TIMESTAMP}.tar.gz"
    DB_DUMP_FILE="/tmp/velo_db_\${TIMESTAMP}.sql"
    mkdir -p "\$BACKUP_DIR"

    echo "Creating backup..."
    echo "  Dumping PostgreSQL..."
    docker compose exec -T postgres pg_dump -U velo velo > "\$DB_DUMP_FILE"

    echo "  Creating archive..."
    tar -czf "\$BACKUP_FILE" \\
        -C /tmp "velo_db_\${TIMESTAMP}.sql" \\
        -C "\$COMPOSE_DIR/backend" ".env"
    rm -f "\$DB_DUMP_FILE"

    # Rotate -- keep last 7 days.
    find "\$BACKUP_DIR" -name "velo_backup_*.tar.gz" -mtime +7 -delete

    echo -e "\${GREEN}✓ Backup: \$BACKUP_FILE\${NC}"
    ls -lh "\$BACKUP_FILE"
}

